package dylan.kwon.toasthelper.core

/**
 * The duration for which the toast is displayed.
 */
enum class ToastDuration {
    /**
     * Displayed for a short duration.
     * The specific duration is typically set by the toast implementation.
     */
    SHORT,

    /**
     * Displayed for a long duration.
     * The specific duration is typically set by the toast implementation.
     */
    LONG
}
